    private void showDetails()
    {
        try{
            con = DriverManager.getConnection("jdbc:mysql://localhost/quiz" , "root" , "");
            pst = con.prepareStatement("select * from question");
            ResultSet resultset = pst.executeQuery();
            ResultSetMetaData metadata = resultset.getMetaData();
            int count = metadata.getColumnCount();
            
            DefaultTableModel dtm = (DefaultTableModel)questiontable.getModel();
            dtm.setRowCount(0);
            
            while(resultset.next())
            {
                Vector v = new Vector();
                for(int i=1;i<=count;i++)
                {
                    v.add(resultset.getInt("id"));
                    v.add(resultset.getInt("quizid"));
                    v.add(resultset.getString("question"));
                    v.add(resultset.getString("choice1"));
                    v.add(resultset.getString("choice2"));
                    v.add(resultset.getString("choice3"));
                    v.add(resultset.getString("choice4"));
                    v.add(resultset.getString("answer"));
                }
                dtm.addRow(v);
            }
            
         
        }
        catch(Exception ex){    System.out.print(ex);}
    }

