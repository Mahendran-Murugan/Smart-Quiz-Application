    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
      
         try{
            con = DriverManager.getConnection("jdbc:mysql://localhost/quiz", "root" , "");
            
            
            DefaultTableModel dtm = (DefaultTableModel) questiontable.getModel();
            int selectedItem = questiontable.getSelectedRow();
            
            String quest = qvar.getText().trim().toLowerCase();
            String ch1 = one.getText().trim().toLowerCase();
            String ch2 = two.getText().trim().toLowerCase();
            String ch3 = three.getText().trim().toLowerCase();
            String ch4 = four.getText().trim().toLowerCase();
            String answer = ans.getText().trim().toLowerCase();
            
            int id = Integer.parseInt(dtm.getValueAt(selectedItem, 0).toString());
            pst = con.prepareStatement("update question set question = ? , choice1 = ? ,choice2 = ? ,choice3 = ? ,choice4 = ? ,answer = ? where id = ? ");
            
            pst.setString(1 , quest);
            pst.setString(2 , ch1);
            pst.setString(3 , ch2);
            pst.setString(4 , ch3);
            pst.setString(5 , ch4);
            pst.setString(6 , answer);
            pst.setInt(7 , id);
            
            
            pst.executeUpdate();
            
            showDetails();
            JOptionPane.showMessageDialog(this , "Successfully Updated...!");
            
            ans.setText("");
            one.setText("");
            two.setText("");
            three.setText("");
            four.setText("");
            qvar.setText("");
            
            
           }
        catch(Exception ex){    System.out.print(ex.getMessage());}
        
    }//GEN-LAST:event_jButton2MouseClicked

